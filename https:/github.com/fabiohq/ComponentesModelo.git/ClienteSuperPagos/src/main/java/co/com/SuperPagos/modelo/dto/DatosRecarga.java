package co.com.SuperPagos.modelo.dto;

import java.io.Serializable;

public class DatosRecarga implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/*
	 * Numero de la recarga
	 */
	private String numero;
	
	/*
	 * Valor de la recarga
	 */
	private String valor;
	
	/*
	 * Terminal suministrado por punto red
	 */
	private String terminal;
	
	/*
	 * Id de transaccion del cliente
	 */
	private String trace;
	
	/*
	 * Codigo del operador Suminitrado por punto Red
	 */
	private String codigoProveedor;
	
	/*
	 * Clave suministrada por punto red
	 */
	private String claveCXR;

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

	public String getTerminal() {
		return terminal;
	}

	public void setTerminal(String terminal) {
		this.terminal = terminal;
	}

	public String getTrace() {
		return trace;
	}

	public void setTrace(String trace) {
		this.trace = trace;
	}

	public String getCodigoProveedor() {
		return codigoProveedor;
	}

	public void setCodigoProveedor(String codigoProveedor) {
		this.codigoProveedor = codigoProveedor;
	}

	public String getClaveCXR() {
		return claveCXR;
	}

	public void setClaveCXR(String claveCXR) {
		this.claveCXR = claveCXR;
	}
	
	
}
