package co.com.SuperPagos.Response;

public class ResponseLogeo {
	
	private String status;
	private user user;
	
	public user getUser() {
		return user;
	}

	public void setUser(user user) {
		this.user = user;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return "ResponseLogeo [status=" + status + ", user=" + user + "]";
	}

	
}
