package co.com.SuperPagos.modelo.dto;

public class DatosRecargaResponse {

	/*
	 * id de transaccion punto red
	 */
	private String idTransaccion;
	
	/*
	 * Mensaje enviado desde punto red
	 */
	private String mensaje;

	
	public String getIdTransaccion() {
		return idTransaccion;
	}

	public void setIdTransaccion(String idTransaccionPuntoRed) {
		this.idTransaccion = idTransaccionPuntoRed;
	}

	public String getMensaje() {
		return mensaje;
	}

	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	
	
}
